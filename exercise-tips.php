<h1>High Intensity Interval Training</h1>

<p>HIIT, in short for high intensity interval training has become very popular in the last year or so, and in my opinion for good reason.  The main reason I love HIIT, is I can give my clients a workout in very little time that will burn as much fat and calories fat and calories as a long steady 45 minute cardio workout .  The key is the intensity of the short workout, the intensity is what drives the afterburn, a bonus you get from HIIT. Some people call HIIT, maximum intensity workouts, because it truly should be your maximum potential for a short time,  followed with a recovery.  That of coarse looks different to a 22 year male and a 61 year old female. But everyone has their own “maximum” level of exertion.  The recommended amount of HIIT workouts for fat burning according to Dr. Len Kravitz, is three times a week for 15 minutes.  Here are some of my favorite HIIT workouts, please note the key is your maximum, so alter the intensity for your present level, but it should feel very challenging, but brief, and remember to warm up and cool down always. </p>

<h2>Treadmill HIIT:</h2>

<ol>
<li>Warm up for 5 minutes, walking then jog</li>
<li>Incline at 1.5 %</li>
<li>Run at your level 7 ( 10 being the highest) for one minute</li>
<li>Walk fast for one minute</li>
<li>Run at your level 8, for one minute</li>
<li>Walk fast for one minute</li>
<li>Run at your level 9 for one minute</li>
<li>Walk fast for one minute</li>
<li>Run at your level 10 for one minute</li>
<li>Walk for one minute</li>
<li>Cool down with slow jog to walk for 5 minutes</li>
</ol>

